第三步，写好单张 PPT


你有个完整的故事线了，现在开始一张张写 PPT。譬如第一张吴某某是谁，第二张陈某某是谁……

单张 PPT 有个基本要求――言之有理、言之有物。

新手可以练习把标题写成一个陈述句，坚决不要写短语（如主要工作、项目概况），这样有助于养成「提炼观点」的能力，而不是堆砌材料。

每画一张 PPT，你要问自己：

这张标题是我想说的意思吗？这个标题能概括下面的内容吗？这个内容能支撑上面的标题吗？

